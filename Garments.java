	{
		this.priceOfJeans=priceOfJeans;
	}
	public void setPriceOfSweater(int priceOfSweater)
	{
		this.priceOfSweater=priceOfSweater;
	}
	public void setColorOfJeans(String colorOfJeans)
	{
		this.colorOfJeans=colorOfJeans;
	}
	public void setColorOfSweater(String colorOfSweater);
	{
		this.colorOfSweater=colorOfSweater;
	}
	public int getPriceOFJeans()
	{
		return this.priceOfJeans;
	}
