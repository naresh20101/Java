class Cow {
    private String color;
    private int Weight;
    private String Type;

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public int getWeight() {
        return Weight;
    }

    public void setWeight(int weight) {
        Weight = weight;
    }

    public String getType() {
        return Type;
    }

    public void setType(String type) {
        Type = type;
    }
}